"""
A program that asks user for two whole numbers
Converts them into integers
Calculates sum,difference and product and prints the results
"""
x = int(input("Enter first number: "))
y = int(input("Enter second number: "))
sum = x + y
difference = x - y
product = x * y
print("Sum: ",sum)
print("Difference: " ,difference)
print("Product: " ,product)